package encapsul;

//Always make instance variable private 
//Always make public modifier in the methods

public class Encapsul_class
    {//start from here 
	
	private int x;
	private int y;
	private int z;
	
	public int getX() 
	{
		return x;
	}
	public void setX(int x) 
	{
		this.x = x;
	}
	public int getY()
	{
		return y;
	}
	public void setY(int y) 
	{
		this.y = y;
	}
	public int getZ() 
	{
		return z;
	}
	public void setZ(int z) 
	{
		this.z = z;
	}
	
	}//End is here
